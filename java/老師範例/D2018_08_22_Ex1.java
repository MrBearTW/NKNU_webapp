// if ... else if .......else
package p1;

public class D2018_08_22_Ex1 {
	public static void main(String[] args) {
		int season = 4;

		if (season == 1) {
			System.out.println("Spring");
		} else if (season == 2) {
			System.out.println("Summer");
		} else if (season == 3) {
			System.out.println("Autumn");
		} else if (season == 4) {
			System.out.println("Winter");
		} else {
			System.out.println("Wrong Number");
		}
	}
}
