package p1;

public class D2018_08_21_Ex6 {
	public static void main(String[] args) {
		int [][] a ={{7, 8, 9, 10}, 
				     {2}, 
				     {3, 4, 5},
				     {1, 3, 5, 7}};
		
		for (int i=0;i<a.length ; i++){
			for (int j=0;j<a[i].length;j++){
				System.out.print(a[i][j]+ " ");
				
			}
			System.out.println();
		}
		
	}

}
