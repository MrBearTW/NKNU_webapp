package p1;

public class D2018_08_21_Ex1 {
//while ... do vs. do ... while
	public static void main(String[] args) {
		
		  int j = 0;
          while (j>1) 
          { //while ... do
        	  System.out.println("hello 1");
        	  
          }
          		
		  do 
		  { //do ... while
			  System.out.println("hello 2");
		  } while(j>1);
          
          
	}

}
