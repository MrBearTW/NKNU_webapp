//switch ... case

package p1;
public class D2108_08_22_Ex2 {

	public static void main(String[] args) {
		int season = 1;
		
		switch(season) {
		
		case 1: 
			System.out.println("Spring");
			//break;
			
		case 2: 
			System.out.println("Summer");
			//break;
		
		case 3: 
			System.out.println("Autumn");
			//break;

		case 4: 
			System.out.println("Winter");
			break;
		
		default:
			System.out.println("Wrong Number");
		}
	}

}
